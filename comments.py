# Define the tank size in gallons
# The total fuel capacity of the vehicle
tank_size = 15
# Define the total distance covered in miles
# The distance the vehicle traveled on a full tank
distance = 600
# Calculate miles per gallon (mpg) by dividing distance by tank size
# This formula gives the efficiency of the vehicle in miles per gallon
mpg = distance / tank_size
# Print the result to display the calculated miles per gallon
# This outputs the efficiency of the vehicle to the console
print("Miles per gallon:", mpg)
# - Higher mpg indicates better fuel efficiency